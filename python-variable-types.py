# Python Variable Types

# Integers
a = 1
b = 2

print(a + b)
# 3

# Floats
a = 1.5
b = 2.2

print(a + b)
# 3.7

# Strings
a = 'A string in single quotes.'
b = "A string in double quotes."
c = "This string is double quoted, but it's cool because it has a single quote."

print(a + ' ' + b)
# A string in single quotes. A string in double quotes.

# find the type of a variable
a = 1
b = 1.5
c = 'two'

print(type(a))
print(type(b))
print(type(c))
# <class 'int'>
# <class 'float'>
# <class 'str'>


